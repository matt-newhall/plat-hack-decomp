#include "macros/btlcmd.inc"

    .data

_000:
    CheckAbility CHECK_HAVE, BTLSCR_ATTACKER, ABILITY_STORM_DRAIN, _076
    CheckAbility CHECK_HAVE, BTLSCR_ATTACKER, ABILITY_WATER_ABSORB, _300
    CheckAbility CHECK_HAVE, BTLSCR_ATTACKER, ABILITY_DRY_SKIN, _300
    PrintAttackMessage
    Wait
    GoTo _224

_076:
    PrintAttackMessage
    Wait
    WaitButtonABTime 15
    CompareMonDataToValue OPCODE_EQU, BTLSCR_ATTACKER, BATTLEMON_SP_ATTACK_STAGE, 12, _204
    PlayBattleAnimation BTLSCR_ATTACKER, BATTLE_ANIMATION_STAT_BOOST
    Wait
    UpdateMonData OPCODE_ADD, BTLSCR_ATTACKER, BATTLEMON_SP_ATTACK_STAGE, 1
    UpdateVar OPCODE_SET, BTLVAR_MSG_TEMP, 4
    // {0}'s {1} raised its {2}!
    PrintMessage BattleStrings_Text_PokemonsAbilityRaisedItsStat_Ally, TAG_NICKNAME_ABILITY_STAT, BTLSCR_ATTACKER, BTLSCR_ATTACKER, BTLSCR_MSG_TEMP
    Wait
    WaitButtonABTime 30

_204:
    UpdateVar OPCODE_FLAG_ON, BTLVAR_MOVE_STATUS_FLAGS, MOVE_STATUS_NO_MORE_WORK
    End

_224:
    UpdateMonDataFromVar OPCODE_GET, BTLSCR_ATTACKER, BATTLEMON_MAX_HP, BTLVAR_HP_CALC_TEMP
    UpdateVar OPCODE_ADD, BTLVAR_HP_CALC_TEMP, 1
    DivideVarByValue BTLVAR_HP_CALC_TEMP, 4
    UpdateVarFromVar OPCODE_SET, BTLVAR_MSG_BATTLER_TEMP, BTLVAR_ATTACKER
    Call BATTLE_SUBSCRIPT_RECOVER_HP
    End

_300:
    UpdateMonDataFromVar OPCODE_GET, BTLSCR_ATTACKER, BATTLEMON_MAX_HP, BTLVAR_HP_CALC_TEMP
    UpdateVar OPCODE_ADD, BTLVAR_HP_CALC_TEMP, 1
    DivideVarByValue BTLVAR_HP_CALC_TEMP, 4
    Call BATTLE_SUBSCRIPT_ABILITY_RESTORES_HP
    End
